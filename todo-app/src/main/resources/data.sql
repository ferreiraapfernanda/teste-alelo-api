DROP TABLE IF EXISTS tarefa;

CREATE TABLE `tarefa` (
	`id` INT NOT NULL AUTO_INCREMENT,
	`titulo` VARCHAR(250) NOT NULL,
	`descricao` VARCHAR(250),
	`dtFim` DATE,
	`status` TEXT(10),
	`dtCadastro` DATE NOT NULL,
	`dtAlteracao` DATE NOT NULL,
	PRIMARY KEY (`id`)
);

